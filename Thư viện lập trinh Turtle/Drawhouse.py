
import turtle
t=turtle.Turtle()
t.speed(10)
def goto(x,y):
    t.penup()
    t.goto(x,y)
    t.pendown()
def vekhoi(a,b,color):
    t.color('black')
    t.fillcolor(color)
    t.begin_fill()
    for i in range(2):
        t.pensize(2)
        t.forward(a)
        t.left(90)
        t.forward(b)
        t.left(90)
    t.end_fill()
def vecay(a,b):
    t.color('black')
    t.fillcolor(b)
    t.begin_fill()
    t.forward(a)
    t.left(120)
    t.forward(a)
    t.left(120)
    t.forward(a)
    t.end_fill()
goto(0,-200)
vekhoi(100,200,'lightblue')
goto(25,-200)
vekhoi(50,100,'navy')
goto(-200,-200)
vekhoi(200,400,'lightblue')
for i in range(4):
    goto(-200,-200+100*i)
    t.forward(200)
    goto(-175,-175+100*i)
    vekhoi(50,50,'white')
    goto(-75,-175+100*i)
    vekhoi(50,50,'white')
goto(-100,200)
t.right(90)
t.forward(400)

goto(200,-100)
t.left(90)
vecay(100,'green')
goto(200,-50)
t.left(120)
vecay(100,'green')
goto(225,-200)
t.left(120)
vekhoi(50,100,'brown')

goto(230,100)
t.fillcolor('yellow')
t.begin_fill()
t.circle(40)
t.end_fill()
turtle.done()